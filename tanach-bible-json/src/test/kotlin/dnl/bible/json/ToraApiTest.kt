package dnl.bible.json

import dnl.bible.api.*
import org.junit.jupiter.api.Assertions.assertEquals
import org.junit.jupiter.api.BeforeAll
import org.junit.jupiter.api.Test
import java.io.File

class ToraApiTest {
    companion object {
        lateinit var tora: Tora
        @BeforeAll @JvmStatic fun setup() {
            val bible = BibleLoader.loadFrom(File("../../json-bible-hebrew/bible-json-files/bible-just_letters-1.0.zip"))
            tora = ToraImpl(bible)
        }
    }

    @Test
    fun testParasha() {
        var parasha = tora.getParasha(ParashaEnum.BALAK)
        var verses = parasha.getVerses()
        assertEquals(104, verses.size)

        parasha = tora.getParasha(ParashaEnum.BEREISHIS)
        verses = parasha.getVerses()
        assertEquals(146, verses.size)

    }

}